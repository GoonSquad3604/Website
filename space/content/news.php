<h2>We completed A-bot!</h2>
<br>
<h3>2/19/2019</h3>
<p>We completed our A-bot for the 2019 competition season. The bot was put in the bag at approximately 11:35 Eastern standard time. Before we bagged N tagged we weighed approximately 110 pounds with all of our components on the robot, except we did not have our hatch manipulator attached to the robot at the time. Overall it was a good build season, and we look forward to the upcoming competition season.</p>
