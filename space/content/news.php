<h2>We Competed at Gibraltar </h2>
<br>
<h3>3/1/2019 - 3/2/2019</h3>
<br>
<p>We completed our week one competition at Gibraltar. We played well seating 2nd out 40 teams overall we 29 ranking points with a average of 2.4 ranking points. Our team made it all the way to the finals, but sadly we lost and did not receive a blue banner. Overall we had a great time and look forward to our next event at Jackson.</p>
